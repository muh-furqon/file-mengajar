# Latihan 1
# Buatlah program yang meminta input dari pengguna berupa nilai ujian
# Jika nilai ujian lebih besar dari 80, tampilkan pesan "Selamat, Anda lulus!"
# Jika nilai ujian kurang dari atau sama dengan 80, tampilkan pesan "Maaf, Anda tidak lulus."

