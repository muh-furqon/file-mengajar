# String adalah tipe data yang digunakan untuk menyimpan teks
# String dapat diapit dengan tanda kutip satu (' ') atau tanda kutip dua (" ")
string = "Halo kawan"
print(string)
print(type(string))

# Integer adalah tipe data yang digunakan untuk menyimpan bilangan bulat
# Integer tidak memiliki tanda kutip
number1 = 10
print(number1)
print(type(number1))

# Walaupun isi variabel adalah angka, jika diapit dengan tanda kutip maka tipe datanya adalah string
coba = "10"
print(coba)
print(type(coba))

# Float adalah tipe data yang digunakan untuk menyimpan bilangan pecahan
# Float memiliki tanda titik (.) sebagai pemisah desimal
number2 = 10.5
print(number2)
print(type(number2))

# Boolean adalah tipe data yang digunakan untuk menyimpan nilai benar atau salah
# Boolean memiliki dua nilai yaitu True dan False
boolean1 = True
print(boolean1)
print(type(boolean1))
boolean2 = False
print(boolean2)
print(type(boolean2))


#OPERASI MATEMATIKA
# Operasi matematika adalah operasi yang dilakukan pada angka
# Operasi matematika terdiri dari penjumlahan, pengurangan, perkalian, pembagian, dan modulus
# Penjumlahan adalah operasi yang dilakukan untuk menjumlahkan dua angka
# Penjumlahan menggunakan tanda tambah (+)
a = 10
b = 5
c = a + b
print(c)

# Pengurangan adalah operasi yang dilakukan untuk mengurangi dua angka
# Pengurangan menggunakan tanda kurang (-)
d = a - b
print(d)

# Perkalian adalah operasi yang dilakukan untuk mengalikan dua angka
# Perkalian menggunakan tanda kali (*)
e = a * b
print(e)

# Pembagian adalah operasi yang dilakukan untuk membagi dua angka
# Pembagian menggunakan tanda bagi (/)
f = a / b
print(f)

# Pembagian bulat adalah operasi yang dilakukan untuk membagi dua angka dan membuang sisa bagi
# Pembagian bulat menggunakan tanda bagi dua (//)
i = 9
j = 2
g = i // j
print(g)

# Modulus adalah operasi yang dilakukan untuk mendapatkan sisa bagi dari dua angka
# Modulus menggunakan tanda persen (%)
h = i % j
print(h)